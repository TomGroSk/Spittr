package pl.gromadzki.spittr.model;

import javax.persistence.*;

@Entity
@Table(name = "TOP_SPITTLERS")
public class TopSpittlers {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    @Column(name = "ID")
    private Integer id;

    @Column(name = "NAME")
    private String name;

    @Column(name = "VALUE")
    private Integer value;

    public TopSpittlers() {
    }

    public TopSpittlers(String name, Integer value) {
        this.name = name;
        this.value = value;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }
}
